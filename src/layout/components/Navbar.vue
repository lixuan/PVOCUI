<template>
  <div class="navbar">
    <hamburger id="hamburger-container" :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb id="breadcrumb-container" class="breadcrumb-container" />

    <div class="right-menu">
      <template>
        <search id="header-search" class="right-menu-item" />

        <lang-select class="right-menu-item hover-effect" />

      </template>

      <el-dropdown class="avatar-container right-menu-item hover-effect" trigger="click">
        <div class="avatar-wrapper">
          <img src="../../assets/login/logo.png" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown">
          <router-link to="/">
            <el-dropdown-item>
              {{ $t('navbar.dashboard') }}
            </el-dropdown-item>
          </router-link>
          <el-dropdown-item><span style="display:block;" @click="onupdatePasswordComp()">{{ $t('navbar.profile') }}</span></el-dropdown-item>
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">{{ $t('navbar.logOut') }}</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
      <update-password ref="updatePasswordComp" :pshow.sync="updatePasswordComp.show" @on-save-success="onSaveSuccess" />
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import ErrorLog from '@/components/ErrorLog'
import LangSelect from '@/components/LangSelect'
import Search from '@/components/HeaderSearch'
import { delCookie } from '@/utils/cookie'
import updatePassword from './updatePassword'
export default {
  components: {
    Breadcrumb,
    Hamburger,
    ErrorLog,
    LangSelect,
    Search,
    updatePassword
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar'
    ])
  },
  data() {
    return {
      updatePasswordComp: {
        show: false
      }
    }
  },
  methods: {
    // 显示详情页面
    onupdatePasswordComp() {
      this.updatePasswordComp.show = true
    },
    // 修改密码成功
    onSaveSuccess() {
      this.logout()
    },
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      delCookie('Abp.AuthToken')
      delCookie('passWord')
      this.$router.push(`/login`)
    }
  }
}
</script>

